class arithmetic_demo
{
	public static void main(String args[])
	{
		int x=100,y=90;
		int sum;
		sum=x+y;
		System.out.println("sum="+sum);
		int sub;
		sub=x-y;
		System.out.println("sub="+sub);
		double div;
		div=x/y;
		System.out.println("div="+div);
		int mul;
		mul=x*y;
		System.out.println("mul="+mul);
	}
}